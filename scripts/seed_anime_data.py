def seed_anime_data():
    conn = get_conn()

    conn.executemany("""
        INSERT OR REPLACE INTO anime_archives
        (pokemon_id, first_appearance, first_appearance_cn, notable_trainer,
         notable_scene, episode_count, notes)
        VALUES (?, ?, ?, ?, ?, ?, ?)
    """, ANIME_DATA)

    conn.commit()
    conn.close()

    print(f"✓ Seeded {len(ANIME_DATA)} anime archive entries")


